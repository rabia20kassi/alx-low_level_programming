#include "3-calc.h"
/**
*op_add - the sum of a and b.
*@a:first number
*@b: second number
*Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - the difference of a and b.
 *@a : first number
 *@b: second number
 *Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
*op_mul - the product of a and b.
*@a:first number
*@b: second number
*Return: the product
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
*op_div - the division of a by b.
*@a: first number
*@b: second number
*Return: the result og the division
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
*op_mod - the remainder of the division of a by b.
*@a:first number
*@b: second number
*Return: the modulo of 2 numbers
*/
int op_mod(int a, int b)
{
	return (a % b);
}
